# Número Inteiro (int)
# O tipo int representa números inteiros (sem casas decimais).
print(10 + 10 + 30)  # Saída: 50

# Número de Ponto Flutuante (float)
# O tipo float representa números com casas decimais.
print(1.5 + 1.5 + 47.0)  # Saída: 50.0

# Booleano (bool)
# O tipo bool representa valores lógicos: True (verdadeiro) ou False (falso).
print(True)   # Saída: True
print(False)  # Saída: False

# String (str)
# O tipo str representa sequências de caracteres. Sempre é deve ser usado com aspas (simples ou duplas).
print("Hello World!")  # Saída: Hello World!
